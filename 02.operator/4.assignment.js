// 할당연산자 (Assignment Operator)
let a = 1;
a = a + 10;
console.log(a);

a += 3; // a = a + 3
console.log(a);

a -= 5; // a = a - 5
console.log(a);

a *= 4; // a = a * 4
console.log(a);

a /= 2; // a = a / 2
console.log(a);

a %= 7; // a = a % 7
console.log(a);

a **= 3; // a = a * a * a
console.log(a);
